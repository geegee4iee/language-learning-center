package model;

public class LichHocManagerModel {
	private int khoaHoc;
	private int buoiHoc;
	private String phongHoc;
	private String thoiGianHoc;
	/**
	 * @return the khoaHoc
	 */
	public int getKhoaHoc() {
		return khoaHoc;
	}
	/**
	 * @param khoaHoc the khoaHoc to set
	 */
	public void setKhoaHoc(int khoaHoc) {
		this.khoaHoc = khoaHoc;
	}
	/**
	 * @return the buoiHoc
	 */
	public int getBuoiHoc() {
		return buoiHoc;
	}
	/**
	 * @param buoiHoc the buoiHoc to set
	 */
	public void setBuoiHoc(int buoiHoc) {
		this.buoiHoc = buoiHoc;
	}
	/**
	 * @return the phongHoc
	 */
	public String getPhongHoc() {
		return phongHoc;
	}
	/**
	 * @param phongHoc the phongHoc to set
	 */
	public void setPhongHoc(String phongHoc) {
		this.phongHoc = phongHoc;
	}
	/**
	 * @return the thoiGianHoc
	 */
	public String getThoiGianHoc() {
		return thoiGianHoc;
	}
	/**
	 * @param thoiGianHoc the thoiGianHoc to set
	 */
	public void setThoiGianHoc(String thoiGianHoc) {
		this.thoiGianHoc = thoiGianHoc;
	}
	
}
