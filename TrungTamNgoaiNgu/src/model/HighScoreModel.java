package model;

public class HighScoreModel {
	private int idHocVien;
	private String hoTen;
	private String tenKyThi;
	private String diem;
	public int getIdHocVien() {
		return idHocVien;
	}
	public void setIdHocVien(int idHocVien) {
		this.idHocVien = idHocVien;
	}
	public String getHoTen() {
		return hoTen;
	}
	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}
	public String getTenKyThi() {
		return tenKyThi;
	}
	public void setTenKyThi(String tenKyThi) {
		this.tenKyThi = tenKyThi;
	}
	public String getDiem() {
		return diem;
	}
	public void setDiem(String diem) {
		this.diem = diem;
	}
}
